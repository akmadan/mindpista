AVAILABLE_ROLES = {
    "therapist": "General therapist for mental health support",
    "meditation": "Meditation guide and mindfulness expert",
    "sleep": "Sleep specialist and wellness coach",
    "anxiety": "Anxiety management specialist",
}


def get_system_prompt(role_type: str) -> str:
    """
    Get the appropriate system prompt based on the role type
    """
    prompts = {
        "therapist": GENERAL_THERAPIST_PROMPT,
        "meditation": MEDITATION_GUIDE_PROMPT,
        "sleep": SLEEP_SPECIALIST_PROMPT,
        "anxiety": ANXIETY_MANAGEMENT_SPECIALIST_PROMPT,
    }

    return prompts.get(role_type.lower(), GENERAL_THERAPIST_PROMPT)
